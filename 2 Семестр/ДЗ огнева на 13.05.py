def linear_search(arr, target):
    """
    Выполняет линейный поиск в списке.

    Args:
        arr: Список для поиска.
        target: Значение, которое нужно найти.

    Returns:
        Индекс целевого значения, если оно найдено, иначе -1.
    """
    # Проходимся по каждому элементу списка
    for i in range(len(arr)):
        # Если текущий элемент равен целевому значению
        if arr[i] == target:
            # Возвращаем индекс найденного элемента
            return i
    # Если цикл завершился, а значение не найдено, возвращаем -1
    return -1  # Целевое значение не найдено

# Пример использования
my_list = [10, 5, 8, 2, 7]
search_value = 8

index = linear_search(my_list, search_value)

if index != -1:
    print(f"Значение {search_value} найдено по индексу {index}")
else:
    print(f"Значение {search_value} не найдено в списке")