# Задание 1.2 Калькулятор процентов


# Запрос ввода числа
try:
    number = float(input("Введите число: "))
except ValueError:
    print("Ошибка: Введите корректное число.")
else:
    # Вывод вариантов действий
    print("Выберите действие:")
    print("1. Найти процент от введённого числа.")
    print("2. Увеличить число на процент.")
    print("3. Уменьшить число на процент.")
    
    # Запрос выбора действия у пользователя
    try:
        choice = int(input("Введите номер действия (1, 2 или 3): "))
        if choice in (1, 2, 3):
            # Запрос ввода процента
            try:
                percent = float(input("Введите процент (неотрицательное значение): "))
                if percent >= 0:
                    if choice == 1:
                        result = number * (percent / 100)
                        print(f"{percent}% от числа {number} равно {result}.")
                    elif choice == 2:
                        result = number * (1 + percent / 100)
                        print(f"{number} увеличено на {percent}% равно {result}.")
                    else:
                        result = number * (1 - percent / 100)
                        print(f"{number} уменьшено на {percent}% равно {result}.")
                else:
                    print("Ошибка: Введите неотрицательное значение процента.")
            except ValueError:
                print("Ошибка: Введите корректное значение процента.")
        else:
            print("Ошибка: Вы выбрали неверное действие.")
    except ValueError:
        print("Ошибка: Введите номер действия числом (1, 2 или 3).")

#Сначала программа запрашивает у пользователя ввод числа с помощью функции input(). Пользователь должен ввести число. Если пользователь ввел некорректное значение (например, текст вместо числа), программа выводит сообщение об ошибке.
#
#Затем программа выводит на экран список доступных действий: нахождение процента от числа, увеличение числа на процент и уменьшение числа на процент.
#
#После вывода списка действий, программа запрашивает у пользователя выбор действия, введенного числом (1, 2 или 3). Если пользователь ввел некорректное значение, программа выводит сообщение об ошибке.
#
#Если пользователь выбрал действие, программа продолжает выполнение. Затем она запрашивает ввод процента, который также должен быть числом. Если пользователь ввел отрицательное значение процента, программа также сообщает об ошибке.
#
#После ввода числа и процента, программа выполняет выбранное действие в соответствии с выбором пользователя. Например, если пользователь выбрал "1. Найти процент от введенного числа", программа вычисляет процент от числа и выводит результат. Аналогично для других действий.
#
#Если пользователь ввел некорректное значение процента (например, текст вместо числа), программа также выводит сообщение об ошибке.