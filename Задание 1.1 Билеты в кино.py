# Задание 1.1 Билеты в кино


cinema_name = input("Введите название кинотеатра: ")
movie_name = input("Введите название фильма: ")

seances = ["10:00", "15:30", "20:15"]

print("Выберите сеанс:")
for i, seance in enumerate(seances, 1):
    print(f"{i}. {seance}")

# Запрос выбора сеанса у пользователя
try:
    choice = int(input("Введите номер сеанса (1, 2 или 3): "))
    if 1 <= choice <= 3:
        chosen_seance = seances[choice - 1]
        print(f"Вы успешно забронировали билеты в кинотеатр “{cinema_name}” на фильм “{movie_name}”.")
        print(f"Сеанс начинается в {chosen_seance}. Приятного просмотра!")
    else:
        print("Ошибка: Вы выбрали несуществующий сеанс.")
except ValueError:
    print("Ошибка: Введите номер сеанса числом (1, 2 или 3).")

#Сначала программа запрашивает у пользователя ввод названия кинотеатра (cinema_name) и названия фильма (movie_name) с помощью функции input(). Пользователь вводит эти данные с клавиатуры.
#
#Затем в коде определен список seances, который содержит доступные сеансы. В данном случае, это три времени начала сеансов: "10:00", "15:30" и "20:15".
#
#Далее программа выводит список доступных сеансов на экран с помощью цикла for, используя функцию enumerate() для нумерации сеансов, начиная с 1. Каждый сеанс выводится на экран вместе с его номером.
#
#После отображения списка сеансов, программа запрашивает у пользователя выбор сеанса с помощью функции input(). Пользователю нужно ввести номер сеанса (1, 2 или 3), который соответствует выбранному сеансу.
#
#Затем программа пытается преобразовать введенное значение в целое число с помощью int(input()). Если пользователь ввел корректное число, то программа проверяет, находится ли это число в диапазоне от 1 до 3 включительно.
#
#Если введенное значение находится в этом диапазоне, то программа выбирает соответствующий сеанс из списка seances с помощью индексации (например, если пользователь выбрал 2, то chosen_seance = seances[1]).
#
#Затем программа выводит сообщение о успешном бронировании билетов, указывая название кинотеатра, название фильма и выбранный сеанс.
#
#Если пользователь ввел число, которое не находится в диапазоне от 1 до 3, программа выводит сообщение об ошибке.
#
#Если пользователь ввел нечисловое значение (например, текст), программа также выводит сообщение об ошибке.